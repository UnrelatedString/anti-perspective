class Observer:
    def __init__(x, y, u):
        self.x = x
        self.y = y
        self.u = u

class Location:
    def __init__(d, theta, phi):
        self.d = d
        self.theta = theta
        self.phi = phi

class VirtualImage:
    def __init__(s, t):
        self.s = s
        self.t = t
